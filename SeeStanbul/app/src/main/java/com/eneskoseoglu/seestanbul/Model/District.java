package com.eneskoseoglu.seestanbul.Model;

public class District {

    private String name;
    private int image;

    public District(String name,int image) {

        this.name = name;
        this.image = image;

    }

    public String getName() {

        return this.name;

    }

    public int getImage() {

        return this.image;

    }

}
